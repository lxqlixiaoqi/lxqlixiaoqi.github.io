// 生成随机数的函数
function getRandomNumber(min, max) {
  return Math.random() * (max - min) + min;
}

// 创建樱花元素的函数
function createSakura() {
  const sakura = document.createElement('div');
  sakura.className = 'sakura';
  const size = getRandomNumber(5, 15);
  sakura.style.width = `${size}px`;
  sakura.style.height = `${size}px`;
  sakura.style.left = `${getRandomNumber(0, window.innerWidth)}px`;
  sakura.style.animationDuration = `${getRandomNumber(5, 15)}s`;
  document.querySelector('.sakura-container').appendChild(sakura);

  // 樱花飘落完成后移除元素
  sakura.addEventListener('animationend', () => {
    sakura.remove();
  });
}

// 定时创建樱花
